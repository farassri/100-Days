
package logika_6;


public class Logika {

    
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        
        boolean hasilAND = a && b;//
        boolean hasilOR = a || b;
        boolean hasilNOT = !a;
        
        System.out.println("Hasil AND: " +hasilAND);
        System.out.println("Hasil OR: " +hasilOR);
        System.out.println("Hasil NOT:  "+hasilNOT);
    }
    
}
