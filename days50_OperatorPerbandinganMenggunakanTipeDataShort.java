public class days50 {
    public static void main(String[] args) {
        //deklarasi variabel short
        short a = 25;
        short b = 40;
        
        //A lebih besar dari nilai B maka hasilnya false
        System.out.println(a > b); 
         //A lebih kecil dari nilai B maka hasilnya true
        System.out.println(a < b);
        //A lebih kecil atau sama dengan nilai B maka hasilnya true
        System.out.println(a <= b);
        //A lebih besar atau sama dengan nilai B maka hasilnya false
        System.out.println(a >= b);
        //A sama dengan nilai B maka hasilnya false
        System.out.println(a == b);
        //A tidak sama dengan nilai B maka hasilnya true
        System.out.println(a != b);
    }
}
