public class days85 {
    public static void main(String[] args) {
        // Perkalian dua bilangan real
        double a = 1.5;
        double b = 2.5;
        double hasil = a * b;
        System.out.println("Hasil perkalian " + a + " dan " + b + " adalah " + hasil); // 3.75

        // Perkalian bilangan bulat dengan bilangan real
        int x = 10;
        double y = 2.5;
        double hasil2 = x * y;
        System.out.println("Hasil perkalian " + x + " dan " + y + " adalah " + hasil2); // 25

    }
}
