public class Days19_Array2Dimensi {

   
    public static void main(String[] args) {
       
        String[][] huruf = {
            {"a", "b", "c"},
            {"D", "F", "G"},
            {"i", "j", "k"},
        };

        // Menampilkan isi array pada indeks ke-[0][1] dan [2][0]
        System.out.println(huruf[0][2]);
        System.out.println(huruf[2][0]);  
    }
}
