package javaapplication157;

public class days80_perulanganDoWhile {
    public static void main(String[] args) {
        //DEKLARASI VARIABEL CHAR
        char huruf = 'A';
       
        do {
            System.out.println("Huruf: "+huruf);//PERULANGAN DO WHILE
            huruf ++;
        } while (huruf <= 'Z');
        
    
    }
}
